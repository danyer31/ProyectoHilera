    void listarRegistroMantenimiento(){
        String sql = "SELECT maquina.Nombre_Maquina, motor.Nombre_Motor, empleado.Nombre_Empleado, mantenimiento.Tipo_Mantenimiento, registro_mantenimiento.Fecha_Mantenimiento, registro_mantenimiento.Descripcion_Mantenimiento FROM registro_mantenimiento INNER JOIN motor on registro_mantenimiento.Id_Motor = motor.Id_Motor INNER JOIN empleado on registro_mantenimiento.Id_Empleado = empleado.Id_Empleado INNER JOIN mantenimiento on registro_mantenimiento.Id_Mantenimiento = mantenimiento.Id_Mantenimiento INNER JOIN maquina on motor.Id_Maquina = maquina.Id_Maquina ORDER BY registro_mantenimiento.Fecha_Mantenimiento ASC";
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[]persona = new Object[6];
            modelo = (DefaultTableModel)tb_registro_mantenimiento.getModel();
            while (rs.next()){
                persona[0]= rs.getString("Nombre_Maquina");
                persona[1] = rs.getString("Nombre_Motor");
                persona[2] = rs.getString("Nombre_Empleado");
                persona[3] = rs.getString("Tipo_Mantenimiento");
                persona[4] = rs.getString("Fecha_Mantenimiento");
                persona[5] = rs.getString("Descripcion_Mantenimiento");
                modelo.addRow(persona);
            }
            tb_registro_mantenimiento.setModel(modelo);
        } catch(Exception e){
        }
    }

