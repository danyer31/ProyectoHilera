    void listarMantenimientos(){
        String sql = "SELECT motor.Fecha_Inicio_Motor, motor.Fecha_Fin_Motor, motor.Id_Motor, motor.Nombre_Motor, maquina.Nombre_Maquina FROM motor INNER JOIN maquina on motor.Id_Maquina = maquina.Id_Maquina ORDER BY motor.Fecha_Fin_Motor ASC";
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[]persona = new Object[5];
            modelo = (DefaultTableModel)tb_Mantenimientos.getModel();
            while (rs.next()){
                persona[0]= rs.getString("Fecha_Inicio_Motor");
                persona[1] = rs.getString("Fecha_Fin_Motor");
                persona[2] = rs.getString("Id_Motor");
                persona[3] = rs.getString("Nombre_Motor");
                persona[4] = rs.getString("Nombre_Maquina");
                modelo.addRow(persona);
            }
            tb_Mantenimientos.setModel(modelo);
        } catch(Exception e){
        }
    }
    
    void modificarFechas(){
        String id = txt_Id_Motor.getText();
        String fechaRealizado = txt_Fecha_Mantenimiento_Realizado.getText();
        String fechaEstimada = txt_Fecha_Mantenimiento_Estimado.getText();
        String sql = "update motor set Id_Motor = '"+id+"',Fecha_Inicio_Motor = '"+fechaRealizado+"',Fecha_Fin_Motor = '"+fechaEstimada+"' where Id_Motor = "+id;
        if(id.equals("")||fechaRealizado.equals("")||fechaEstimada.equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar fechas");
        }else{
            try{
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Mantenimiento Realizado");
                limpiarTablaMantenimientos();
            }catch(Exception e){
            }
        }    
    }
    
    void limpiarTablaMantenimientos(){
        for(int i = 0; i<=tb_Mantenimientos.getRowCount();i++){
            modelo.removeRow(i);
            i = i-1;
        }
    }
    
