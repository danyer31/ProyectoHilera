    void AgregarRegistro(){
        String id = txt_Id_Motor.getText();
        String empleado = combo_empleado.getItemAt(combo_empleado.getSelectedIndex()).getCodigo();
        String mantenimiento = combo_tipo_mantenimiento.getItemAt(combo_tipo_mantenimiento.getSelectedIndex()).getCodigo();
        String fechaRealizado = txt_Fecha_Mantenimiento_Realizado.getText();
        //String fechaEstimada = txt_Fecha_Mantenimiento_Estimado.getText();
        String descripcion = txt_Descripcion_Mantenimiento.getText();
        if(id.equals("")||empleado.equals("")||mantenimiento.equals("")||fechaRealizado.equals("")||descripcion.equals("")){
            JOptionPane.showMessageDialog(null, "Debe llenar el formulario");
        } else{
            String sql = "insert into registro_mantenimiento(Id_Motor, Id_Empleado, Id_Mantenimiento, Fecha_Mantenimiento, Descripcion_Mantenimiento)values('"+id+"','"+empleado+"','"+mantenimiento+"','"+fechaRealizado+"','"+descripcion+"')";
            try{
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Mantenimiento Realizado");
                limpiarTablaMantenimientos();
            }catch(Exception e){      
            } 
        }
    }

